import { NextRequest, NextResponse } from "next/server";
import bcrypt from "bcryptjs";
import { DatabaseOperations } from "@/shared/lib/database-operations";

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { name, email, password } = body;

    // Validate input
    if (!name || !email || !password) {
      return NextResponse.json(
        { error: "Name, email, and password are required" },
        { status: 400 }
      );
    }

    if (password.length < 6) {
      return NextResponse.json(
        { error: "Password must be at least 6 characters" },
        { status: 400 }
      );
    }

    // Check if user already exists
    const existingUser = await DatabaseOperations.findUserByEmail(email);
    if (existingUser) {
      return NextResponse.json(
        { error: "User with this email already exists" },
        { status: 409 }
      );
    }

    // Hash password
    const passwordHash = await bcrypt.hash(password, 12);

    // Create user
    const user = await DatabaseOperations.createUser(name, email, passwordHash);

    // Create default user preferences
    await DatabaseOperations.updateUserPreferences(user.id, {
      theme: "light",
      notificationsEnabled: true,
      emailNotifications: true,
      dataSharingConsent: false,
    });

    // Return user data (without password hash)
    return NextResponse.json(
      {
        success: true,
        user: {
          id: user.id,
          name: user.name,
          email: user.email,
        },
      },
      { status: 201 }
    );
  } catch (error) {
    console.error("Registration error:", error);
    return NextResponse.json(
      { error: "Internal server error" },
      { status: 500 }
    );
  }
}
